package com.spring.project.board.model;

public class FreVO {
	private int fre_number;
	private String fre_category;
	private String fre_title;
	private String fre_content;
	
	public int getFre_number() {
		return fre_number;
	}
	public void setFre_number(int fre_number) {
		this.fre_number = fre_number;
	}
	public String getFre_category() {
		return fre_category;
	}
	public void setFre_category(String fre_category) {
		this.fre_category = fre_category;
	}
	public String getFre_title() {
		return fre_title;
	}
	public void setFre_title(String fre_title) {
		this.fre_title = fre_title;
	}
	public String getFre_content() {
		return fre_content;
	}
	public void setFre_content(String fre_content) {
		this.fre_content = fre_content;
	}
	
}
	
